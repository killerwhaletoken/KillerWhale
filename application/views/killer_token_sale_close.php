function calculate_data(type)
{
    var kwt_rate = $("#kwt_rate").val();
    var discount_perctange = $("#discount_perctange").val();

    var usd_val = $("#usd_id").val();
    var kwt_val = $("#kwt_id").val();

    if(type == 1)
    {
        var total_kwt = parseFloat(usd_val)/parseFloat(kwt_rate);
        if(isNaN(total_kwt))
        {
            $("#kwt_id").val(0);
        }
        else
        {
            $("#kwt_id").val(total_kwt);
        }
    }
    else if(type == 2)
    {
        var total_usd = parseFloat(kwt_val)*parseFloat(kwt_rate);

        if(isNaN(total_usd))
        {
            $("#usd_id").val(0);
        }
        else
        {
            $("#usd_id").val(total_usd);           
        }
    }

    var usd_data = $("#usd_id").val();

    var final_amount = parseFloat(usd_data) - ((parseFloat(usd_data)*discount_perctange)/100);
    if(isNaN(final_amount))
    {    
        $("#kwt_rate_html").html('0 USD');
    }
    else
    {
        $("#kwt_rate_html").html(final_amount+' USD');   
    }

    $(".amount_class").val(final_amount);

    $(".total_token_class").val($("#kwt_id").val());

}

